#include "libft.h"

void	*ft_bzero(void *s, size_t num)
{
	size_t	i;
	char	*tmp;

	tmp = (char*)s;
	i = 0;
	while (i < num)
	{
		tmp[i] = '\0';
		i++;
	}
	s = tmp;
	return (s);
}
